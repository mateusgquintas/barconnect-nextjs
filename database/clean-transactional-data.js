async function countRecords(table) {
  try {
    const { count, error } = await supabase
      .from(table)
      .select('*', { count: 'exact', head: true });
    
    if (error) {
      console.warn(`   ‚ö†Ô∏è  ${table}: erro ao contar (${error.message})`);
      return 0;
    }
    return count || 0;
  } catch (err) {
    console.warn(`   ‚ö†Ô∏è  ${table}: erro ao contar (${err.message})`);
    return 0;
  }
}

async function deleteAllFromTable(table, description) {
  try {
    console.log(`\nüóëÔ∏è  Limpando ${description}...`);
    
    // Contar registros antes
    const countBefore = await countRecords(table);
    console.log(`   üìä Registros encontrados: ${countBefore}`);
    
    if (countBefore === 0) {
      console.log(`   ‚úÖ Tabela j√° est√° vazia`);
      return { success: true, deleted: 0 };
    }

    // Deletar todos os registros
    const { error, count } = await supabase
      .from(table)
      .delete()
      .neq('id', '00000000-0000-0000-0000-000000000000'); // Delete all (PostgreSQL idiom)

    if (error) {
      console.error(`   ‚ùå Erro: ${error.message}`);
      return { success: false, error: error.message };
    }

    console.log(`   ‚úÖ ${countBefore} registros removidos`);
    return { success: true, deleted: countBefore };
  } catch (err) {
    console.error(`   ‚ùå Erro inesperado: ${err.message}`);
    return { success: false, error: err.message };
  }
}

